package Practice;

import java.util.Scanner;

public class Swapping {
	public static void main(String[] args) {
		
		 int a=20;
		 int b=30;
		 
		System.out.println("a ="+ a);
		 System.out.println("b ="+ b );
		 
		 a=a+b; //1+2=3
		 b=a-b; //3-2=1
		 a=a-b; //3-1=2
		 
		 System.out.println("After swapping :");
		 System.out.println();
		 System.out.println("a ="+ a );
		 System.out.println("b ="+ b );
		 
		 
	}

}
